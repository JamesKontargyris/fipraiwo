<h2>Costs</h2>

<p>The EDT charges a preferential rate to the Fipra Network. Editorial and Design services cost €56.25 per hour/pro rata based on time used or €450 per day. Translation services cost between 0.10-0.15 cent per word. Cost estimates can be given in advance of agreeing work.</p>

<p class="italic">Please Note: We will be able to handle most requests on the same day, but a surcharge of 20% will be applied to all requests that require a deadline of within 24 hours of the work commencing.</p>

