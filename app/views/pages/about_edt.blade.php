<h2>What is the EDT?</h2>

<p>The Editorial, Design and Translation Team (EDT) is a group of trained and qualified editors, designers  and translators who offer a number of professional editorial, graphic design  and translation services to the Fipra Network.</p>
<p>The EDT ensures that all Fipra's written and presentational output including e-mails, letters, proposals, PowerPoint presentations, reports, monitors, booklets, newsletters, websites and more:</p>

<ul class="square">
    <li>have a polished, professional and finished look;</li>
    <li>have consistent English language grammar;</li>
    <li>adhere to the Fipra Editorial and Style Guidelines;</li>
    <li>are perceived externally as being of consistently high quality;</li>
    <li>are translated correctly.</li>
</ul>

<p>We would like all Fipra Units, Correspondents and Special Advisers to embrace this cost-effective resource by using the EDT's full range of editorial, graphic design and translation capabilities. The EDT is also at your disposal for output that is not Fipra branded: for example, for your local brand or the branding of a client.</p>

<h2>What can the EDT do for you?</h2>

<h3>Editorial Services</h3>

<p>Re-writing, Copy-editing, Proofreading, ‘Quick Edit’ Service (new service offering quick spelling and grammar check and ensuring adherence to Fipra’s editorial guidelines)</p>

<p>These services are already employed by Fipra for the following types of output:</p>
<ul class="square">
    <li>Monitors</li>
    <li>Publications (brochures, leaflets, newsletters)</li>
    <li>Booklets & other publications</li>
    <li>Research findings</li>
    <li>PowerPoint presentations</li>
    <li>Reports</li>
    <li>Tender documents and all proposals</li>
    <li>Press releases</li>
    <li>Website content</li>
    <li>Recruitment notices</li>
    <li>Biographies or CVs</li>
</ul>

<h3>Graphic Design Services</h3>

<p>Design is central to delivering a polished, professional product. Compelling imagery and a complementary graphic style make an impact-a key objective for any brand. The EDT offers a broad range of graphic services, including concept, design, realisation and the production of printed, online and electronic materials.</p>

<p>Our design services can be used on their own or in tandem with editorial services for the following kinds of output:</p>

<ul class="square">
    <li>Design and layout of printed publications</li>
    <li>PowerPoint presentations</li>
    <li>Logos or emblems</li>
    <li>Newsletters, e-Newsletters, e-Cards</li>
    <li>Photo/image search, selection and optimisation</li>
    <li>Labels and stickers</li>
    <li>Promotional materials, e.g. for seminars and conferences</li>
    <li>Design and layout of printed publications</li>
    <li>Website design</li>
    <li>Social media creation</li>
    <li>Charts, graphs, vector graphics</li>
    <li>Apps</li>
</ul>

<h3>Translation Services</h3>

<p>Our translation service is a new offering we have recently added to the EDT. We now have access to many professional and experienced translators from all over the world. Our translators can translate documents, presentations, proposals and any other materials you require into their own native language.</p>

