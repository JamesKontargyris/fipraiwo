<p>Fiplex is a team of trained and qualified lawyers which offers a professional and experienced legal service to the Fipra Network on an on-demand basis.  Fiplex is an innovative approach to legal services for the growing Fipra Network, allowing business lawyers with substantial private practice or in-house experience to provide corporate counsel services as needed at a fraction of the cost of hiring a full time executive level general counsel or relying exclusively on law firm counsel.</p>

<p>Fiplex exists to provide a business-focussed and practical approach to any matters requiring legal input within the organisation, but essentially to:</p>
<ul class="square">
    <li>ensure that all contracts and engagements are properly reviewed and provide adequate protection, including ethical codes;</li>
    <li>provide proactive legal risk management;</li>
    <li>protect valuable intellectual property;</li>
    <li>identify and solve business issues;</li>
    <li>avoid the financial burden of full-time legal staff but add value to the organisation.</li>
</ul>

<h2>How do I use Fiplex?</h2>
<p>To use Fiplex you can either complete the <a href="fiplex">Fiplex Internal Work Order form</a> or e-mail the team on
    <a href="mailto:fiplex@fipra.com">fiplex@fipra.com</a>. To commission Fiplex you will need to fill in a short insturction similar to an internal work order, this instruction sheet will be sent to you by the Fiplex co-ordinator in respone to any email request. The Fiplex co-ordinator will confirm receipt of your request and deadline by return e-mail as fast as we can. A lawyer will then be assigned to the matter will commence.</p>

<h2>Confidentiality</h2>
<p>All members of Fiplex sign a personal non-disclosure agreement with financial penalties for non¬observance. The work you commission is treated confidentially by all of us and is not shared with anyone.</p>

<h2>Costs</h2>
<p>Fiplex charges a preferential rate to the Fipra Network of €125 per hour/pro rata based on time used or €1,000 per day. Cost estimates can be given in advance of agreeing work.</p>

<h2>Invoicing</h2>
<p>Fiplex will invoice monthly to Fipra International who will onward bill to clients, cost centres or Members of the Fipra Network as appropriate.  The Fiplex operates as a "Special Adviser" would within the Network.</p>

<h2>Working Hours</h2>
<p>Fiplex is currently made up of qualified lawyers and in time will have members of the team all over Europe.  Currently we work to the following office hours: Monday-Friday 09.00-18.00 Central European Time (CET).</p>

<p>This window allows us to cater for regular and urgent advisory work in most timezones. In some cases we can be flexible on these hours to accommodate your timings and deadlines -please contact us to discuss your time frame if your instructions are time-sensitive.</p>

<p>Please submit your request at your earliest opportunity to ensure we can plan accordingly and allocate the most suited team members.</p>

